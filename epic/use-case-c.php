		<h2>Use Case-C</h2>

		<p></p><strong>Use Case:</strong> It is 9:30 am on a Wednesday at the Department of Animal Welfare (City of Albuquerque). Ellen just received word that three dogs and one cat that were rescued from a hoarding case over the weekend are ready to be placed up for adoption.  Since the shelter took in 14 cats and dogs from the rescue, the shelter is at capacity with some animals sharing a kennel designed for single occupancy. Ellen would like to get these animals adopted into their forever homes as soon as possible.

		In addition to adding these pets to the Department of Animal Welfare (City of Albuquerque) website, Ellen discovers the Pet Foster ABQ website, which will allow her to reach a broader audience.</p>

		<h2>Interaction flow for Ellen Nicol</h2>
		<ul>
			<li>Ellen is in her office and googles "Pet Foster ABQ"</li>
			<li>The search returns the website</li>
			<li>Ellen creates a profile on the website to post animals for adoption </li>
			<li>The site thanks her for creating a profile and returns her to a page to post pet adoptions</li>
			<li>Ellen enters in all the information for the first dog with the option to add more animals</li>
			<li>Ellen clicks "add" and enters the information for the second dog </li>
			<li>Ellen clicks "add" and enters the information for the third dog </li>
			<li>Ellen clicks "add" and enters the information for the cat </li>
			<li>Ellen clicks post<li>
			<li>The site posts the information to the adoptable pets section of the website</li>
		</ul>
